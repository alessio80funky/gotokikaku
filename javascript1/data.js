//データ型

/*
基本型　（プリミティブ型）

1. number型　（数値型）=> 数字のこと

2. string型　(文字列)　=> テキスト

3. boolean型　（判断型）　=> true/false 真・偽

4. 特殊型　=> null (ヌル　-> データが存在しないという意味)　・　undefined (定義されていないデータを示す)

*/

let num = 10;//number型

let str = "アレッシオ";

let boolean1 = true;//boolean型
let boolean2 = false;//boolean型

let name = null;//特殊型
let lastName = undefined;//特殊型

/*

参照型

array型　(配列)　=> データの集合

object型　（オブジェクト）=> データの集合

*/

